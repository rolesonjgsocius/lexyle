  <script>
    const carousel = document.querySelector('#industrialslider');

let startX = 0;
let isDragging = false;

// Handle both mouse and touch events
const startDrag = (x) => {
    isDragging = true;
    startX = x;
};

const endDrag = () => {
    isDragging = false;
};

const handleDrag = (x) => {
    if (!isDragging) return;

    const diff = x - startX;

    if (diff > 50) {
        // Scroll left - Trigger previous slide
        $('.carousel').carousel('prev');
        isDragging = false; // Reset dragging
    } else if (diff < -50) {
        // Scroll right - Trigger next slide
        $('.carousel').carousel('next');
        isDragging = false; // Reset dragging
    }
};

// Mouse Events
carousel.addEventListener('mousedown', (e) => startDrag(e.pageX));
carousel.addEventListener('mousemove', (e) => handleDrag(e.pageX));
carousel.addEventListener('mouseup', endDrag);
carousel.addEventListener('mouseleave', endDrag);

// Touch Events
carousel.addEventListener('touchstart', (e) => startDrag(e.touches[0].pageX));
carousel.addEventListener('touchmove', (e) => handleDrag(e.touches[0].pageX));
carousel.addEventListener('touchend', endDrag);

    </script>
